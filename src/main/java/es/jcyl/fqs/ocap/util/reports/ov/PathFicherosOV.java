 package es.jcyl.fqs.ocap.util.reports.ov;
 
 public class PathFicherosOV
 {
   String pathBase;
   String pathRelativoFichero;
   String directorioRaiz;
   String nombreFichero;
 
   public void setPathBase(String pathBase)
   {
     this.pathBase = pathBase;
   }
 
   public String getPathBase()
   {
     return this.pathBase;
   }
 
   public void setPathRelativoFichero(String pathRelativoFichero)
   {
     this.pathRelativoFichero = pathRelativoFichero;
   }
 
   public String getPathRelativoFichero()
   {
     return this.pathRelativoFichero;
   }
 
   public void setDirectorioRaiz(String directorioRaiz)
   {
     this.directorioRaiz = directorioRaiz;
   }
 
   public String getDirectorioRaiz()
   {
     return this.directorioRaiz;
   }
 
   public void setNombreFichero(String nombreFichero)
   {
     this.nombreFichero = nombreFichero;
   }
 
   public String getNombreFichero()
   {
     return this.nombreFichero;
   }
 }

